#include <stdio.h>
#include <stdlib.h>
#include "arithmatic.h"

int addNum(int a, int b)
{
	int nAdd = a + b;
	return nAdd;
}
int subNum(int a, int b)
{
	int nsub = a - b;
	return nsub;
}
int mulNum(int a, int b)
{
	int nmul = a * b;
	return nmul;
}
int divNum(int a, int b)
{
	int ndiv = a / b;
	return ndiv;
}
int mdNum(int a, int b)
{
	int nmd = a % b;
	return nmd;
}
